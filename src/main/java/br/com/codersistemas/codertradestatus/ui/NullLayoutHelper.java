package br.com.codersistemas.codertradestatus.ui;

public class NullLayoutHelper {

	private int x;
	private int y;
	private int w;
	private int h;
	private int s;

	public NullLayoutHelper(int s, int x, int y) {
		this.s = s;
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y += h += s;
	}

	public int setW(int w) {
		this.w = w;
		return w;
	}

	public int setH(int h) {
		this.h = h;
		return h;
	}

}
